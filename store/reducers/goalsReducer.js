const initialState = {
    allGoals: getSessionGoals(),
}

const goalsReducer =(state = initialState, action) => {
    switch (action.type) {
        case FETCH_GOALS:
            const newState = action.payload;
            return newState;
            break
        case UPDATE_GOAL:
            const updatedGoals = state.allGoals;
            updatedGoals[payload.id] = payload;
            newState = { updatedGoals }; //should it be const newState?
            return newState;
            break
        default:
            return state;
    }
};

export default goalsReducer;